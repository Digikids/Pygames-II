# 1. Write a program that takes in a user's age
# and checks if they are old enough to vote (18 years or older).
print('QUESTION 1')
print('What is your age?')
age = input()
if int(age) >= 18:
    print('You are old enough to vote!')
else:
    print('You are not old enough to vote.')

